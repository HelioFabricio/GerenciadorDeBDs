    public void atualizarColunas(){
        try {
            String tableName = conexao.getTableName();
            String dbName = conexao.getDbName();
            System.out.println("Nomes: " +tableName + " " + dbName);
            
            conexao.conectarBancoEspecifico(dbName);
            model = conexao.getTableModel(dbName, tableName);
            
            limpaColunasDoModel(model);
            jTabelaInsercao.setModel(model);
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(GuiBancos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void getInfoTabela(){
        int linha = 0;
        for (int i = 0; i < model.getColumnCount(); i++) {
            nomesColunas += model.getColumnName(i) + " ";
            insercao += (String)model.getValueAt(linha, i) + " ";
        }
        
        System.out.println(insercao);
    }
    
    public void limpaColunasDoModel(DefaultTableModel model){
        model.setNumRows(0);
        int numeroColunas = model.getColumnCount();
        String info = "";
        for(int i = 0; i < numeroColunas-1; i++){
            info += ""+ ",";
        }
        model.addRow(new Object[]{});
        System.out.println(info);
    }
